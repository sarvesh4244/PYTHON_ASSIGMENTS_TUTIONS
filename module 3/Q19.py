# What is a tuple? Difference between list and tuple.

# A tuple is a data structure in Python that is similar to a list in some ways but has some key differences. Here are the main characteristics and differences between lists and tuples:

# 1. Mutability:

# List: Lists are mutable, which means you can change their contents (add, remove, or modify elements) after creation.
# Tuple: Tuples are immutable, which means once you create a tuple, you cannot change its elements. You can't add, remove, or modify elements in a tuple.