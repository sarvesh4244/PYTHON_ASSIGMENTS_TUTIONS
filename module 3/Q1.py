# What is a List? How will you reverse a list?  
# n computer programming, a list is a commonly used data structure that represents an ordered collection of elements. Lists are used to store and manage multiple items of data in a single variable. The elements in a list can be of the same or different data types, and they are typically indexed by their position within the list.

my_list = [1, 2, 3, 4, 5]
reversed_list = my_list[::-1]
print(reversed_list)  